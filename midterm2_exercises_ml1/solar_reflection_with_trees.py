def get_matrix_and_vector():
    data = get_dataset()
    X = [row[:-1] for row in data]
    y = [row[-1] for row in data]
    return X, y


def train_test_split(data, test_size):
    size_split = int((1.0-test_size) * len(data))
    x_test = [row[:-1] for row in data[:size_split]]
    x_train = [row[:-1] for row in data[size_split:]]
    y_test = [row[-1] for row in data[:size_split]]
    y_train = [row[-1] for row in data[size_split:]]
    return x_train, x_test, y_train, y_test


